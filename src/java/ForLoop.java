package java;

public class ForLoop {

	public static void main(String[] args) {
		// print name for 5 times
		for(int i=1;i<=5;i++)
		{
			System.out.println("Ranga:::"+i);
		}
		System.out.println("================================");
		for(int i=1;i<=5;i+=2)
		{
			System.out.println("Ranga:::"+i);
		}
	}

}
